package com.shikikan.gflcompanionapp;

public class Shots {
    int total, hits, misses;

    Shots() {
        total = 0;
        hits = 0;
        misses = 0;
    }

    public int getTotal(){
        return total;
    }

    public int gethits(){
        return hits;
    }

    public int getMisses(){
        return misses;
    }
}
